'''
Python = Linguagem de programção
Tipo de tipagem = Dinâmica/Forte
str -> string -> Texto
Strings são textos que estão dentro de aspas simples ou duplas
'''

# Aspas simples
print('Hello, World!')
# Aspas duplas
print("Hello, World!")
# Escape
print("hello, \"world\"!") # a \ serve como escape para as aspas duplas/simples
# r
print(r"hello, \"world\"!") # o r antes da string faz com que o escape não funcione

# Tanto faz usar aspas simples ou duplas, Escape e r funcionam em ambos os casos